/*
 * Functions associated with file signitures or magic numbers.
 */

#[derive(PartialEq, Debug)]
enum FileSigniture {
    Unknown,
    MultiBitBitcoinWallet,
    ArmoredPGPPublicKey,
    SQLiteDatabase,
    TelegramDesktopFile,
    TelegramDesktopEncryptedFile,
    JKSJavaKeyStore,
    PEMCertificate,
    PEMCertificateRequest,
    PEMPrivateKey,
    PEMDSAPrivateKey,
    PEMRSAPrivateKey,
    PuTTYPrivateKeyV2,
    PuTTYPrivateKeyV3,
    OpenSSHPrivateKey,
    WindowsRegistry,
    KDBX,
}

impl FileSigniture {
    fn from_bytes(inital_file_bytes: &Vec<u8>) -> Self {
        FileSigniture::Unknown
    }
}

